    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
    try{
        themeSelected = "";
        typologySelected = "";
        extensionSelected = "";
        stateSelected = "";
        rateSelected = "";
        themeFlag = false;
        typologyFlag = false;
        extensionFlag = false;
        stateFlag = false;
        rateFlag = false;
        
        
        //TUTTI I DOCUMENTI
        ArrayList<Document> documents = Document.getDocuments(conn);
        listModelDocuments = new MyListModel();        
        for(Document d : documents){
            listModelDocuments.getListModel().addElement(d);
            listDocuments.setModel(listModelDocuments.getListModel());
        }


        //FILTRI TEMATICHE
        ArrayList<Theme> themes = Theme.getAllThemes(conn);
        DefaultComboBoxModel cbModelThemes = new DefaultComboBoxModel();
        cbModelThemes.addElement("");
        for(Theme t : themes){
            cbModelThemes.addElement(t.toString());
            boxTheme.setModel(cbModelThemes);
        }  
            
        //FILTRI TIPOLOGIE
        ArrayList<Typology> typologies = Typology.getAllTypologies(conn);
        DefaultComboBoxModel cbModelTyp = new DefaultComboBoxModel();
        cbModelTyp.addElement("");
        for(Typology t : typologies){
            cbModelTyp.addElement(t.toString());
            boxTypology.setModel(cbModelTyp);
        }
 
        //FILTRI ESTENSIONE
        ArrayList<String> extensions = Document.getAllExtensions(conn);
        DefaultComboBoxModel cbModelExtensions = new DefaultComboBoxModel();
        cbModelExtensions.addElement("");
        for(String s : extensions){
            cbModelExtensions.addElement(s);
            boxExtension.setModel(cbModelExtensions); 
        }

        }catch(SQLException e){
            System.out.println(e.getMessage());
        }catch(NullPointerException e){
            System.out.println(e.getMessage());
        }catch(ClassNotFoundException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_formWindowOpened

