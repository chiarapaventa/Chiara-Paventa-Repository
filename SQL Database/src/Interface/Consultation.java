    private void findButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findButtonActionPerformed
        try{
            String search = searchField.getText();
            listModelDocuments.setFilterSearchBar(conn, search);
            
            if(themeSelected.compareTo("") != 0)
                listModelDocuments.setFilterTheme(themeSelected);
            if(typologySelected.compareTo("") != 0)
                listModelDocuments.setFilterTheme(themeSelected);
            if(extensionSelected.compareTo("") != 0)
                listModelDocuments.setFilterTheme(extensionSelected);
            if(stateSelected.compareTo("") != 0)
                listModelDocuments.setFilterTheme(stateSelected);
            if(rateSelected.compareTo("") != 0)
                listModelDocuments.setFilterTheme(rateSelected);
            
            listDocuments.setModel(listModelDocuments.getListModel());
            
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }catch(ClassNotFoundException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_findButtonActionPerformed

