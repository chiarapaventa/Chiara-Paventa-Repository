    private void boxThemeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxThemeActionPerformed
        //Theme theme = theme.getName();
        themeSelected =  (String) boxTheme.getSelectedItem();
        try{
            if(themeSelected.compareTo("") == 0){
                themeFlag = false;
                listModelDocuments.removeFilter(conn, "theme", themeSelected, typologySelected, 
                                                extensionSelected, stateSelected, rateSelected);
                listDocuments.setModel(listModelDocuments.getListModel());
            }else{
                if(themeFlag){
                    listModelDocuments.removeFilter(conn, "theme", "", typologySelected, 
                                                    extensionSelected, stateSelected, rateSelected);
                }
                listModelDocuments.setFilterTheme(themeSelected);
                listDocuments.setModel(listModelDocuments.getListModel());
                themeFlag = true;
            }
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }catch(ClassNotFoundException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_boxThemeActionPerformed

